#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	void	*ptr;

	ptr = b;
	while (len != 0)
	{
		*(unsigned char *)b = c;
		b++;
		len--;
	}
	return (ptr);
}
